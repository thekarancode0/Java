public class switchCase {
    public static void main(String[] args) {
        int option = 5;
        switch (option){
            case 5:
                System.out.println("option 5");
//                break;
            case 4:
                System.out.println("option 4");
//                break;

            case 3:
                System.out.println("option 3");
                break;
            case 1:
                System.out.println("Option 1");
                break;

            default:
                System.out.println("Not matched");
        }
    }
}
